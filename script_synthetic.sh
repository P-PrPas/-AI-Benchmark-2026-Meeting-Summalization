#!/bin/bash
#SBATCH -p gpu
#SBATCH -N 1 -c 16
#SBATCH --gpus-per-task=1
#SBATCH --ntasks-per-node=1
#SBATCH -t 08:00:00
#SBATCH -A zz991011
#SBATCH -J synthetic_data
#SBATCH -o /project/zz991000-zdeva/zz991011/CAMNET_P/logs/slurm-%j.out
#SBATCH -e /project/zz991000-zdeva/zz991011/CAMNET_P/logs/slurm-%j.err

set -euo pipefail

REPO_ROOT="/project/zz991000-zdeva/zz991011/CAMNET_P"
MODEL_ROOT="/project/zz991000-zdeva/zz991011/models"
CACHE_ROOT="/project/zz991000-zdeva/zz991011/.cache"
CONDA_ENV_NAME="three_env"

SYNTH_SOURCE_TRAIN_JSON_PATH="${SYNTH_SOURCE_TRAIN_JSON_PATH:-$REPO_ROOT/data/train/train_set.json}"
SYNTH_OUTPUT_JSON_PATH="${SYNTH_OUTPUT_JSON_PATH:-$REPO_ROOT/data/train/train_set_synthetic.json}"
SYNTH_MERGED_OUTPUT_JSON_PATH="${SYNTH_MERGED_OUTPUT_JSON_PATH:-$REPO_ROOT/data/train/train_set_merged.json}"
SYNTH_MODEL_NAME_OR_PATH="${SYNTH_MODEL_NAME_OR_PATH:-$MODEL_ROOT/gemma-4-31B-it}"
SYNTH_CACHE_DIR="${SYNTH_CACHE_DIR:-$CACHE_ROOT/synthetic_generation}"
SYNTH_NUM_DOCS_TO_SAMPLE="${SYNTH_NUM_DOCS_TO_SAMPLE:-5}"
SYNTH_NUM_QUERIES_PER_DOC="${SYNTH_NUM_QUERIES_PER_DOC:-4}"
SYNTH_MAX_PARAGRAPHS_PER_PROMPT="${SYNTH_MAX_PARAGRAPHS_PER_PROMPT:-4}"
SYNTH_MAX_ATTEMPTS_PER_QUERY="${SYNTH_MAX_ATTEMPTS_PER_QUERY:-3}"
SYNTH_MAX_NEW_TOKENS="${SYNTH_MAX_NEW_TOKENS:-512}"
SYNTH_TEMPERATURE="${SYNTH_TEMPERATURE:-0.35}"
SYNTH_TOP_P="${SYNTH_TOP_P:-0.9}"
SYNTH_SEED="${SYNTH_SEED:-42}"

mkdir -p "$REPO_ROOT/logs" "$(dirname "$SYNTH_OUTPUT_JSON_PATH")" "$(dirname "$SYNTH_MERGED_OUTPUT_JSON_PATH")"
cd "$REPO_ROOT"

ml Mamba

export HF_HOME="$CACHE_ROOT"
export HF_HUB_CACHE="$CACHE_ROOT"
export HF_DATASETS_CACHE="$CACHE_ROOT/datasets"
export HF_HUB_OFFLINE=1
export HF_DATASETS_OFFLINE=1
export TRANSFORMERS_OFFLINE=1
export PYTHONUNBUFFERED=1

export CAMNET_SYNTH_SOURCE_TRAIN_JSON_PATH="$SYNTH_SOURCE_TRAIN_JSON_PATH"
export CAMNET_SYNTH_OUTPUT_JSON_PATH="$SYNTH_OUTPUT_JSON_PATH"
export CAMNET_SYNTH_MODEL_NAME_OR_PATH="$SYNTH_MODEL_NAME_OR_PATH"
export CAMNET_SYNTH_CACHE_DIR="$SYNTH_CACHE_DIR"
export CAMNET_SYNTH_NUM_DOCS_TO_SAMPLE="$SYNTH_NUM_DOCS_TO_SAMPLE"
export CAMNET_SYNTH_NUM_QUERIES_PER_DOC="$SYNTH_NUM_QUERIES_PER_DOC"
export CAMNET_SYNTH_MAX_PARAGRAPHS_PER_PROMPT="$SYNTH_MAX_PARAGRAPHS_PER_PROMPT"
export CAMNET_SYNTH_MAX_ATTEMPTS_PER_QUERY="$SYNTH_MAX_ATTEMPTS_PER_QUERY"
export CAMNET_SYNTH_MAX_NEW_TOKENS="$SYNTH_MAX_NEW_TOKENS"
export CAMNET_SYNTH_TEMPERATURE="$SYNTH_TEMPERATURE"
export CAMNET_SYNTH_TOP_P="$SYNTH_TOP_P"
export CAMNET_SYNTH_SEED="$SYNTH_SEED"
export CAMNET_SYNTH_LOCAL_FILES_ONLY=1

export CAMNET_MERGE_ORIGINAL_TRAIN_JSON_PATH="$SYNTH_SOURCE_TRAIN_JSON_PATH"
export CAMNET_MERGE_SYNTHETIC_JSON_PATH="$SYNTH_OUTPUT_JSON_PATH"
export CAMNET_MERGE_OUTPUT_JSON_PATH="$SYNTH_MERGED_OUTPUT_JSON_PATH"
export CAMNET_MERGE_STRICT_DOC_MATCH=1
export CAMNET_MERGE_SKIP_DUPLICATE_DOC_QUERY=1

echo "Job starts at: $(date)"
echo "Running on node: $(hostname)"
echo "Repo root: $REPO_ROOT"
echo "Synthetic model path: $SYNTH_MODEL_NAME_OR_PATH"
echo "Synthetic output path: $SYNTH_OUTPUT_JSON_PATH"
echo "Merged output path: $SYNTH_MERGED_OUTPUT_JSON_PATH"

conda run -n "$CONDA_ENV_NAME" python -u synthetic/generate_dataset.py
conda run -n "$CONDA_ENV_NAME" python -u synthetic/merge_datasets.py

echo "Job finished at: $(date)"
